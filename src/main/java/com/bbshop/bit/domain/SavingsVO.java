package com.bbshop.bit.domain;

import java.util.Date;

public class SavingsVO {

	Date or_date;
	String or_items;
	int or_savings;
	int or_num;
	int or_savings_Total;

	public Date getOr_date() {
		return or_date;
	}
	public void setOr_date(Date or_date) {
		this.or_date = or_date;
	}
	public String getOr_items() {
		return or_items;
	}
	public void setOr_items(String or_items) {
		this.or_items = or_items;
	}
	public int getOr_savings() {
		return or_savings;
	}
	public void setOr_savings(int or_savings) {
		this.or_savings = or_savings;
	}
	public int getOr_num() {
		return or_num;
	}
	public void setOr_num(int or_num) {
		this.or_num = or_num;
	}
	public int getOr_savings_Total() {
		return or_savings_Total;
	}
	public void setOr_savings_Total(int or_savings_Total) {
		this.or_savings_Total = or_savings_Total;
	}
	
}
